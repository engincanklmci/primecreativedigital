// Helper function to get project by slug
export const getProjectBySlug = (slug) => {
  return portfolioProjects.find(project => project.slug === slug);
};

// Helper function to get related projects
export const getRelatedProjects = (currentProjectId, category) => {
  return portfolioProjects.filter(project => 
    project.id !== currentProjectId && project.category === category
  ).slice(0, 3);
};

// Helper function to get all projects for portfolio page
export const getPortfolioProjects = () => {
  return portfolioProjects.map(project => ({
    id: project.id,
    slug: project.slug,
    title: project.title,
    category: project.category,
    image: project.featuredImage,
    description: project.description,
    tags: project.tags
  }));
};
